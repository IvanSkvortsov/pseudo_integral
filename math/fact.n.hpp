template<typename T>
T const & math::fact_n(int n)
{
	if( n < 0 )
	{
		std::cerr << "Error: [fact_n] negative n : " << n << std::endl;
		exit(1);
	}
	else if( n > nmax<T>() )
	{
		std::cerr << "Error: [fact_n] n/NMAX : " << n << "/" << nmax<T>() << std::endl;
		exit(1);
	}
	else
		return fact_n_data<T>(n);
}

